    private void RgsUserMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_RgsUserMousePressed
        if (RgsUser.getText().equals("Username")) {
            RgsUser.setText("");
            RgsUser.setForeground(Color.black);
        }
        if (String.valueOf(RgsPass.getPassword()).isEmpty()) {
            RgsPass.setText("************");
            RgsPass.setForeground(Color.gray);
        }
    }//GEN-LAST:event_RgsUserMousePressed

    private void RgsPassMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_RgsPassMousePressed
        if (String.valueOf(RgsPass.getPassword()).equals("************")) {
            RgsPass.setText("");
            RgsPass.setForeground(Color.black);
        }
        if (RgsUser.getText().isEmpty()) {
            RgsUser.setText("Username");
            RgsUser.setForeground(Color.gray);
        }
        
    }//GEN-LAST:event_RgsPassMousePressed

