    public void AgregarUsuario(){
       
        String SQL = "insert into usuarios values(?,?,?)";
        
        try{
            
            PreparedStatement pat = ubd.prepareStatement(SQL);
            String pass = String.valueOf(RgsPass.getPassword());
            
            pat.setString(1, "0");
            pat.setString(2, RgsUser.getText().trim());
            pat.setString(3, pass);
            pat.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Registro Exitoso");
            
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Error en el registro" +e.getMessage());
        }
        Inicio inc = new Inicio();
        inc.setVisible(true);
        this.setVisible(false);
         
    }
